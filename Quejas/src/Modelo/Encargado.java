package Modelo;

/**
 *
 * @author jorge diaz
 */
public class Encargado {
    int id_ecargado;
    String area;
    String cargo;
    int cedula;

    public int getId_ecargado() {
        return id_ecargado;
    }

    public void setId_ecargado(int id_ecargado) {
        this.id_ecargado = id_ecargado;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public String getCargo() {
        return cargo;
    }

    public void setCargo(String cargo) {
        this.cargo = cargo;
    }

    public int getCedula() {
        return cedula;
    }

    public void setCedula(int cedula) {
        this.cedula = cedula;
    }
    
    
}
